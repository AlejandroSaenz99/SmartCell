    private void BotonReimprimirMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotonReimprimirMouseClicked
PrinterJob job=PrinterJob.getPrinterJob();
       
       job.setPrintable(plantillaTickets2);
       if(job.printDialog()){
           try {
              PrintRequestAttributeSet aset = new HashPrintRequestAttributeSet();

                aset.add(OrientationRequested.PORTRAIT);
                aset.add(MediaSizeName.ISO_C5);
                   
                job.print(aset);
           } catch (PrinterException ex) {
               System.out.println("catch");           }
       }
       else{
           JOptionPane.showMessageDialog(null, "La impresion se Cancel√≥");}         
    }//GEN-LAST:event_BotonReimprimirMouseClicked

