    private void BotonMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_BotonMouseClicked
        if ((ComboMarca2.getSelectedItem().toString().equals("Marca")) || (txtModelo.getText().isEmpty())) {
            JOptionPane.showMessageDialog(null, "Por favor elija una marca y escriba un modelo");
        } else {
            String BusquedaModelo = Metodos.Buscar_Modelo(ComboMarca2.getSelectedItem().toString(), txtModelo.getText());
            if (BusquedaModelo.equals("Datos encontrados")) {
                JOptionPane.showMessageDialog(null, "Modelo ya existe");
                txtModelo.setText("");
            } else {
                DModelos Modelo = new DModelos();
                Modelo.setMarca(ComboMarca2.getSelectedItem().toString());
                Modelo.setModelo(txtModelo.getText());
                Met.GuardarModelo(Modelo);
                txtModelo.setText("");
                LlenarComboMarca();
            }
        }
    }//GEN-LAST:event_BotonMouseClicked

