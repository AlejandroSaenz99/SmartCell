    private void Boton5MouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_Boton5MouseClicked
        if (ComboMarca.getSelectedItem().toString().equals("Marca")) {
            JOptionPane.showMessageDialog(null, "Por favor seleccione una marca");
        } else {
            Metodos Met = new Metodos();
            Met.EliminarMarca(ComboMarca.getSelectedItem().toString());
            LlenarComboMarca();
            ac = Met.autocompletarMarca(txtMarca);
        }        // TODO add your handling code here:
    }//GEN-LAST:event_Boton5MouseClicked

